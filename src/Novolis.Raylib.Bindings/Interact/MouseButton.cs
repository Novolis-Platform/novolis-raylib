namespace Novolis.Raylib.Interact;

/// <summary>Mouse button identifiers aligned with raylib.</summary>
public enum MouseButton
{
    /// <summary>Primary (left) button.</summary>
    Left = 0,

    /// <summary>Secondary (right) button.</summary>
    Right = 1,

    /// <summary>Middle (wheel) button.</summary>
    Middle = 2,

    /// <summary>Side button 1.</summary>
    Side = 3,

    /// <summary>Side button 2.</summary>
    Extra = 4,

    /// <summary>Forward side button.</summary>
    Forward = 5,

    /// <summary>Back side button.</summary>
    Back = 6,
}
